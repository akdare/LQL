     */
    public function store(Request $request, $post_id)
    {
        $post=Post::find($post_id);
        $comment=new Comment();
        $comment->comment=request('comment');
        $comment->user_id=Auth::user()->id;

       // dd($comment);
        $post->comments()->save($comment);
        
        return back();
    }

